package org.tcse.algorithms.learn.logparser;

public class NVPair {
	private final String name;
	private final String value;

	public NVPair(String name, String value) {
		this.name = name;
		this.value = value;
	}

	public String getName() {
		return name;
	}

	public String getValue() {
		return value;
	}

}
